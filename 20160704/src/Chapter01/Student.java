package Chapter01;
// 04. 실습1 - Data Type
public class Student {
	String number;		// 학번 - 맨 앞에 0이 붙을 수 있으므로 integer가 아닌 String 형태로
	String name;		// 이름
	boolean isEnrolld;	// 등록 여부
	double grade;		// 평점
	String address;		// 주소
	String major;		// 전공
	int unit;			// 이수 학점
	boolean haveMinor;	// 부전공 여부
	String juminNo;		// 주민번호(-없이) - 맨 앞에 0이 붙을 수 있으므로 integer가 아닌 String 형태로
	String cellphone;	// 핸드폰 번호(-없이) - 맨 앞에 0이 붙을 수 있으므로 integer가 아닌 String 형태로
	int age;			// 나이
	String email;		// 이메일 주소
}